num1 = 8
num2 = 6
suma = num1 + num2
resta = num1 - num2
multiplicacion = num1 * num2
division = num1 / num2
modulo = num1 % num2
division_entera = num1 // num2
potencia = num1 ** num2
resultado1 = "{}+{}={}".format(num1, num2, suma)
resultado2 = "{}-{}={}".format(num1, num2, resta)
resultado3 = "{}*{}={}".format(num1, num2, multiplicacion)
resultado4 = "{}/{}={:.2f}".format(num1, num2, division)
resultado5 = "{}%{}={}".format(num1, num2, modulo)
resultado6 = "{}//{}={}".format(num1, num2, division_entera)
resultado7 = "{}**{}={}".format(num1, num2, potencia)
print(resultado1)
print(resultado2)
print(resultado3)
print(resultado4)
print(resultado5)
print(resultado6)
print(resultado7)